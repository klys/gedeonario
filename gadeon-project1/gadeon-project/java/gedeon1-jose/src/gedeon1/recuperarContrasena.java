    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // nueva contraseña recuperada
        int mayusculas, minusculas, maximo, minimo;
        ResultSet rs = db.Select("tbl_conf_clave", "long_min, long_max, cant_mayus, cant_minus", "activo = '1'");
        maximo = 8;
        minimo = 4;
        mayusculas = 0;
        minusculas = 0;
        
        try {
            if (rs.next()) {
                mayusculas = rs.getInt("cant_mayus");
                minusculas = rs.getInt("cant_minus");
                maximo = rs.getInt("long_max");
                minimo = rs.getInt("long_min");
            }
        } catch (SQLException ex) {
            Logger.getLogger(admin_usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (jPasswordField3.toString().equals("")||jPasswordField4.toString().equals("")){
            JOptionPane.showMessageDialog(null, "Todos los campos deben estar llenos", "Acción no Ejecutada", JOptionPane.ERROR_MESSAGE);      
               
                }else if(!jPasswordField3.getText().equals(jPasswordField4.getText())){
            JOptionPane.showMessageDialog(null, "Los Campos nueva contraseña y  confirmar contraseña deben ser iguales", "Acción no Ejecutada", JOptionPane.ERROR_MESSAGE);      
                  
                }   else if (jPasswordField3.getText().toString().length()>maximo || jPasswordField3.getText().toString().length()<minimo) {
                JOptionPane.showMessageDialog(null,"la clave debe poseer entre "+minimo+" y "+maximo+" caracteres");
                
            } else if (db.val.contar_mayusculas(jPasswordField3.getText()) < mayusculas) {
                JOptionPane.showMessageDialog(null,"La contraseña debe tener al menos"+mayusculas+"letars en mayusculas");
            }else if(db.val.contar_minusculas(jPasswordField3.getText()) < minusculas){
                JOptionPane.showMessageDialog(null,"La contraseña debe tener al menos"+minusculas+"letars en minusculas" );
            }else{
            if (this.db.Update("usuarios", "clave = '"+this.db.getStringMessageDigest(jPasswordField3.getText(), "md5")+"'", "id_usuario = "+this.usuario_id)) {
                JOptionPane.showMessageDialog(null, "Clave cambiada efectivamente");
                jTabbedPane1.setSelectedIndex(0);
                this.restablecerFormulario();
            }
        }
        
        
    }//GEN-LAST:event_jButton6ActionPerformed

