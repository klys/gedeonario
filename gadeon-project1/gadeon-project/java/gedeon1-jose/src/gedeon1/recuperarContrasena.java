    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // encontrar nombre de usuario o contraseña
   
        switch(jComboBox1.getSelectedItem().toString()) {
            case "Cedula":
                try {
                   
                   this.usuario_id = this.db.Select2("usuarios", "id_usuario", "cedula = '"+jTextField1.getText()+"'").getInt("id_usuario");
                    
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "La cedula introducida no existe, vuelva a intentarlo. (0x0)");
                    Logger.getLogger(recuperarContrasena.class.getName()).log(Level.SEVERE, null, ex);
                } catch (NullPointerException ex) {
                    JOptionPane.showMessageDialog(null, "La cedula introducida no existe, vuelva a intentarlo.(0x1)");
                    Logger.getLogger(recuperarContrasena.class.getName()).log(Level.SEVERE, null, ex);
                }
                if(this.usuario_id > 0) {
                    jTabbedPane1.setSelectedIndex(1);
                }
                break;
            case "Nombre":
                try {
                       this.usuario_id = this.db.Select2("usuarios", "id_usuario", "nombre = '"+jTextField1.getText()+"'").getInt("id_usuario");
                       if (this.usuario_id > 0) {
                            jTabbedPane1.setEnabledAt(1, true);
                       }

                } catch (SQLException ex) {
                    Logger.getLogger(recuperarContrasena.class.getName()).log(Level.SEVERE, null, ex);
                }
                    
                
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // decision del usuario
        switch(jComboBox2.getSelectedItem().toString()) {
            case "Cambiar Contraseña":
        try {
            this.clave_vieja = db.Select2("usuarios","clave", "id_usuario="+usuario_id).getString("clave");
        } catch (SQLException ex) {
            Logger.getLogger(recuperarContrasena.class.getName()).log(Level.SEVERE, null, ex);
        }
                jTabbedPane1.setSelectedIndex(3);
                
                break;
            case "Recuperar Contraseña":
                Connection con;
                con = this.db.getConexion();
                
                
            try {
                Statement stmt = con.createStatement();
                ResultSet rs = stmt.executeQuery("SELECT ps.nb_pregunta_secreta as pregunta, pg.tx_respuesta as respuesta FROM tbl_preg_usr pg"
                            + " INNER JOIN preguntas_secretas ps ON ps.id_preguntas_secreta = pg.id_pregunta_secreta"
                            + " WHERE pg.id_usuario = "+this.usuario_id);
                if (rs.next()) {
                    jLabel7.setText(rs.getString("pregunta"));
                    this.respuesta_secreta = rs.getString("respuesta");
                    jTabbedPane1.setSelectedIndex(2);
                }
                stmt.close();
            } catch (SQLException ex) {
                Logger.getLogger(recuperarContrasena.class.getName()).log(Level.SEVERE, null, ex);
            }
            
                break;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

