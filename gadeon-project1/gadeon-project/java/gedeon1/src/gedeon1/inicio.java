    private void boton_acaeptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_boton_acaeptarMouseClicked
String estatus, usr, clv;
        
        usr = usuario.getText();
        clv = clave.getText();

        if (usr.equals("") || clv.equals("")) {
            mensaje.setVisible(true);
            mensaje.setText("Todos los campos deben estar llenos");
            mensaje.setForeground(Color.RED);
        } else {
            
            boolean existe = m.Select3("usuarios", "*", "tx_login='" + usr + "' and clave='" + clv + "'");
            if (existe == true) {
                
                try {
                    int id_rol = m.Select2("usuarios", "*", "tx_login='" + usr + "' and clave='" + clv + "'").getInt("id_rol_usr");
                    estatus=m.Select2("usuarios", "*", "tx_login='" + usr + "' and clave='" + clv + "'").getString("id_estatus");
                    String nb_estatus=m.Select2("tbl_estatus", "*", "id_estatus='" + estatus + "'").getString("nb_estatus");
                    if(nb_estatus.equals("inactivo")){
                        JOptionPane.showMessageDialog(null , "Su cuenta en el sistema a si desacctivada, comuniquese con su profesor");
                        usuario.setText("");
                        clave.setText("");
                        mensaje.setVisible(false);
                        
                    }else{
                    
                        String id_usuario = m.Select2("usuarios", "*", "tx_login='" + usr + "' and clave='" + clv + "'").getString("id_usuario");
                        
                        m.id_usuario=id_usuario;
                        int id_admin, id_estu, id_maestro;
                        id_admin = m.Select2("tbl_rol_usr", "*", "nb_rol_usr='Administrador'").getInt("id_rol_usr");
                        id_estu = m.Select2("tbl_rol_usr", "*", "nb_rol_usr='Estudiante'").getInt("id_rol_usr");
                        id_maestro = m.Select2("tbl_rol_usr", "*", "nb_rol_usr='Maestro'").getInt("id_rol_usr");
                        String id_est_admin, id_est_estu, id_est_maestro, nb_est_admin, nb_est_estu,nb_est_maestro;
                            id_est_admin = m.Select2("tbl_rol_usr", "*", "nb_rol_usr='Administrador'").getString("id_estatus");
                        id_est_estu = m.Select2("tbl_rol_usr", "*", "nb_rol_usr='Estudiante'").getString("id_estatus");
                        id_est_maestro = m.Select2("tbl_rol_usr", "*", "nb_rol_usr='Maestro'").getString("id_estatus");
                        nb_est_admin=m.Select2("tbl_estatus", "*", "id_estatus='" + id_est_admin + "'").getString("nb_estatus");
                        nb_est_estu=m.Select2("tbl_estatus", "*", "id_estatus='" + id_est_estu + "'").getString("nb_estatus");
                        nb_est_maestro=m.Select2("tbl_estatus", "*", "id_estatus='" + id_est_maestro + "'").getString("nb_estatus");
                          
                        if(nb_est_admin.equals("inactivo")&&id_rol==id_admin){
                        JOptionPane.showMessageDialog(null , "Se ha negado el accseso a los usuarios de tipo administrador");   
                        }else if(nb_est_maestro.equals("inactivo")&&id_rol==id_maestro){
                            JOptionPane.showMessageDialog(null , "Se ha negado el accseso a los usuarios de tipo profesor");   
                        }else if(nb_est_estu.equals("inactivo")&&id_rol==id_estu){
                            JOptionPane.showMessageDialog(null , "Se ha negado el accseso a los usuarios de tipo estudiante");   
                        }
//aqui......
                        if (id_rol == id_admin || id_rol == id_maestro) {
                           
                        if(clv.equals("12345")){
                             JOptionPane.showMessageDialog(null , "usted ha sido inscrito recientemente... a continuacion culminara su fase de registro");
                             admin_usuario ad = new admin_usuario (m);
                             ad.setVisible(true);
                             m.setClave(clv);
                              m.setNombre_usuario(usr);
                                this.dispose();
                              
                        }else{
                            m.id_rol=id_rol;
                            m.setNombre_usuario(usr);
                            m.setClave(clv);
                            String nombre_rol=m.Select2("tbl_rol_usr", "*", "id_rol_usr="+id_rol+"").getString("nb_rol_usr");
                            m.setTipo(nombre_rol);
                            menu_principal mp = new menu_principal(m);
                            mp.setVisible(true);
                            this.dispose();
                            
                        } 
                        }
                            
                        else if("12345".equals(clv)) {
                                JOptionPane.showMessageDialog(null , "usted ha sido inscrito recientemente... a continuacion culminara su fase de registro");
                                String nombre_rol=m.Select2("tbl_rol_usr", "*", "id_rol_usr="+id_rol+"").getString("nb_rol_usr");
                                m.id_rol=id_rol;
                                m.setTipo(nombre_rol);
                                  m.setNombre_usuario(usr);
                            m.setClave(clv);
                                admin_usuario ad = new admin_usuario (m);
                                ad.setVisible(true);
                                this.dispose();
                                
                            } else{
                            menu_juego mj = new menu_juego();
                              m.setNombre_usuario(usr);
                            m.setClave(clv);
                            mj.setVisible(true);
                            this.dispose();
                            
                        
                        
                        
                        
                        }  
                        }
                        
                    } catch (SQLException ex) {                
                    Logger.getLogger(inicio.class.getName()).log(Level.SEVERE, null, ex);
                }
         
                }else {
                mensaje.setVisible(true);
                mensaje.setText("Usuario o Clave incorrecta");
                mensaje.setForeground(Color.RED);
                }
            }         // TODO add your handling code here:
    }//GEN-LAST:event_boton_acaeptarMouseClicked

